    private void exportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Specify a file save");
        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();

            try {
                FileWriter fw = new FileWriter(fileToSave);
                BufferedWriter bw = new BufferedWriter(fw);

                for (int i = 0; i < tbl_table.getRowCount(); i++) {
                    for (int j = 0; j < tbl_table.getColumnCount(); j++) {
                        bw.write(tbl_table.getValueAt(i, j).toString());
                    }
                    bw.newLine();
                }

                JOptionPane.showMessageDialog(this, "SUCCESSFULLY LOADED", "INFORMATION", JOptionPane.INFORMATION_MESSAGE);

                // Close the BufferedWriter to flush and close the file
                bw.close();
            } catch (IOException ex) {
                Logger.getLogger(student_home_page.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_exportActionPerformed

