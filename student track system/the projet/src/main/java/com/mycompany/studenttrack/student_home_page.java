    private void get_infoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_get_infoActionPerformed
        // TODO add your handling code here:
        String id = txt_id.getText();
        student_register student = DB_management.getInf(id);
        if (student != null) {
            txt_name_surname.setText(student.name_surname);
            txt_phone.setText(student.phone);
            txt_mail.setText(student.email);
            String cours = String.valueOf(student.cours);
            switch (cours) {
                case "0":
                    txt_cours.setText("calculus ||");
                    break;
                case "1":
                    txt_cours.setText("differential equations");
                    break;
                case "2":
                    txt_cours.setText("computer programming (java)");
                    break;
                case "3":
                    txt_cours.setText("computer programming ( C++)");
                    break;
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "ID is not found");
        }

    }//GEN-LAST:event_get_infoActionPerformed

